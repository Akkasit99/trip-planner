-- ลบตารางเก่าถ้ามี
DROP TABLE IF EXISTS activities;
DROP TABLE IF EXISTS foods;
DROP TABLE IF EXISTS attractions;
DROP TABLE IF EXISTS provinces;

-- สร้างตาราง provinces
CREATE TABLE provinces (
    id TEXT PRIMARY KEY,
    name TEXT NOT NULL,
    name_en TEXT,
    region TEXT NOT NULL,
    description TEXT,
    image_url TEXT,
    coordinates JSONB,
    created_at TIMESTAMP WITH TIME ZONE DEFAULT NOW(),
    updated_at TIMESTAMP WITH TIME ZONE DEFAULT NOW()
);

-- สร้างตาราง attractions
CREATE TABLE attractions (
    id TEXT PRIMARY KEY,
    name TEXT NOT NULL,
    name_en TEXT,
    province_id TEXT REFERENCES provinces(id),
    type TEXT,
    description TEXT,
    coordinates JSONB,
    rating DECIMAL(2,1),
    estimated_duration INTEGER,
    entrance_fee INTEGER,
    opening_hours TEXT,
    created_at TIMESTAMP WITH TIME ZONE DEFAULT NOW(),
    updated_at TIMESTAMP WITH TIME ZONE DEFAULT NOW()
);

-- สร้างตาราง foods
CREATE TABLE foods (
    id TEXT PRIMARY KEY,
    name TEXT NOT NULL,
    name_en TEXT,
    province_id TEXT REFERENCES provinces(id),
    type TEXT,
    description TEXT,
    price_range JSONB,
    recommended_places JSONB,
    created_at TIMESTAMP WITH TIME ZONE DEFAULT NOW(),
    updated_at TIMESTAMP WITH TIME ZONE DEFAULT NOW()
);

-- สร้างตาราง activities
CREATE TABLE activities (
    id TEXT PRIMARY KEY,
    name TEXT NOT NULL,
    name_en TEXT,
    province_id TEXT REFERENCES provinces(id),
    type TEXT,
    description TEXT,
    coordinates JSONB,
    duration INTEGER,
    difficulty_level TEXT,
    price_range JSONB,
    season TEXT,
    created_at TIMESTAMP WITH TIME ZONE DEFAULT NOW(),
    updated_at TIMESTAMP WITH TIME ZONE DEFAULT NOW()
);

